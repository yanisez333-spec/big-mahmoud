let currentLang: Lang = (localStorage.getItem('bm_lang') as Lang) ?? 'fr'

export function t(key: string): string {
  return TRANSLATIONS[currentLang][key] ?? key
}

export function getLang(): Lang { return currentLang }

export function applyLang(lang: Lang): void {
  currentLang = lang
  localStorage.setItem('bm_lang', lang)
  const html = document.documentElement
  const info = LANGS.find(l => l.code === lang)!
  html.setAttribute('lang', lang)
  html.setAttribute('dir', info.dir)

  document.querySelectorAll<HTMLElement>('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n!
    const val = t(key)
    if (el instanceof HTMLInputElement || el instanceof HTMLTextAreaElement) {
      el.placeholder = val
    } else if (el instanceof HTMLSelectElement) {
      // handled per-option
    } else {
      el.textContent = val
    }
  })

  // Handle select options with data-i18n-option
  document.querySelectorAll<HTMLOptionElement>('[data-i18n-option]').forEach(opt => {
    opt.textContent = t(opt.dataset.i18nOption!)
  })

  document.querySelectorAll<HTMLElement>('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang)
  })
}

export function initLangSwitcher(): void {
  document.querySelectorAll<HTMLElement>('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => applyLang(btn.dataset.lang as Lang))
  })
  applyLang(currentLang)
}
